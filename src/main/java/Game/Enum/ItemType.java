package Game.Enum;

/**
 * Represents the type of item in the game.
 *
 * <p>This enum defines the different types of items that can be used in the game.
 * Each item type has specific characteristics and effects on the game.</p>
 *
 * @Author ESTG Diogo Pereira Batista LSIRC - 8230367
 * @Author ESTG Rodrigo Fernandes Ribeiro LSIRC - 8190315
 */
public enum ItemType {
    /**
     * Represents an item that heals the player.
     * <p>Items of this type are used to restore the player's health.</p>
     */
    HEALER,

    /**
     * Represents an item that provides armor to the player.
     * <p>Items of this type are used to increase the player's defense.</p>
     */
    ARMOR
}